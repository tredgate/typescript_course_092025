const exponentiation = 2 ** 5; // 2 na 5
console.log("Mocnění 2^5 = " + exponentiation);

const modulo = 5 % 2; // Zbytek po dělení 5 / 2, výsledek: 1
console.log("Modulo 5 % 2 = " + modulo);

let increment = 5;
increment++; // Inkrement: navýší increment o 1 (výsledek: 6)
console.log("increment++ = " + increment);
